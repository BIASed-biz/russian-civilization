export function genericIntroRu(country, count) {
  // simple Russian plural for "figure(s)"
  const n = count % 100
  const n1 = count % 10
  let word = 'выдающихся деятелей'
  if (n < 11 || n > 14) {
    if (n1 === 1) word = 'выдающегося деятеля'
    else if (n1 >= 2 && n1 <= 4) word = 'выдающихся деятеля'
  }
  return {
    h: `${country} — деятели с русскими и советскими корнями`,
    p: [
      `Эта страница документирует ${count} ${word}, связанных со страной ${country}, чьи биографии связаны с Российской империей, Советским Союзом или государствами-преемниками — через рождение, происхождение родителей или более раннюю родословную. Каждый профиль снабжён источниками и фиксирует конкретное происхождение и связь.`,
    ],
  }
}
